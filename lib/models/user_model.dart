class UserModel {
  final String name;
  final int age;
  final String gender;
  final String phone;
  final String email;
  final String bankName;
  final String bankAccount;
  final String designation;

  UserModel({
    required this.name,
    required this.age,
    required this.gender,
    required this.phone,
    required this.email,
    required this.bankName,
    required this.bankAccount,
    required this.designation,
  });
}
